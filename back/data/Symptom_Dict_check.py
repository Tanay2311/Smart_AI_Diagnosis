def find_common_values(symptom_map):
    """Finds and prints values that appear in both keys and values."""
    
    # 1. Create a set from the keys for efficient lookup
    keys_set = set(symptom_map.keys())
    
    # 2. Create a set from the unique values
    values_set = set(symptom_map.values())
    
    # 3. Find the intersection of the two sets
    common_items = keys_set.intersection(values_set)
    
    if common_items:
        print("ü§ù Common values found in both keys and values:")
        # Print as a sorted list for readability
        print(sorted(list(common_items)))
    else:
        print("ü§∑ No common values were found.")

# Run the function with your map
